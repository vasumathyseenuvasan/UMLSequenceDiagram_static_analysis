
public class VariableDecl {

	private String variableParentType;
	private String variableClassType;
	private String variableName;
	
	public String getVariableParentType() {
		return variableParentType;
	}
	public void setVariableParentType(String variableParentType) {
		this.variableParentType = variableParentType;
	}
	public String getVariableClassType() {
		return variableClassType;
	}
	public void setVariableClassType(String variableClassType) {
		this.variableClassType = variableClassType;
	}
	public String getVariableName() {
		return variableName;
	}
	public void setVariableName(String variableName) {
		this.variableName = variableName;
	}
	
	
	
}
